   <div class="simple-content"
	
		<span class="title">What benefit does Kiirti provide to citizens?</span><br><br><span class="copy">
Citizens can use Kiirti to report about any civic and governance related issue using phone (voice), sms (text), email and the web. In the backend, we partner with different government, non-government and civil society organizations to help moderate, aggregate and initiate action on the reported issues.
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">How does Kiirti help non-government or civil society organizations?</span><br><br><span class="copy">
Non-government and civil society organizations can setup a specific Supported Cause on Kiirti to enable volunteers or citizen users to report issues concering their area of focus.<br><br>
<i>For example, an organization focused on addressing eve-teasing, can setup a Supported Cause on Kiirti (in a few minutes) and allow citizen users to start reporting about eve-teasing and harassment issues through phone, sms, email and web immediately. The organization's moderator can use Kiirti's admin panel to moderate the incoming reports, aggregate issues, initiate action and provide updates regularly.</i>
		</span><br><img src='/media/img/line.gif'><br>

		<span class="title">Does Kiirti guarantee resolution for reported issues?</span><br><br><span class="copy">
No. Unfortunately, we cannot guarantee resolution for issues raised on the platform. Our primary focus is to enable easy reporting of civic and governance issues through many channels. However, by partnering with media and other organizations (including some government organizations) we strive to push for resolution for majority of the reported issues.
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">How do you validate the authenticity of reported issues?</span><br><br><span class="copy">
Whenever a new report gets submitted, it goes through 2 stages of filtering. <br>
In the first stage, the moderator for the Supported Cause against which the report was submitted, will check for appropriateness (relevance, non-bias, etc) of the content. Once a report clears this stage, it will be published on the site. <br>
In the second stage, based on related news articles, other users comments and reports, the moderator can validate authenticity of the report. Once a report is authenticated, it'll be marked as verified on top of the report page.
		</span><br><img src='/media/img/line.gif'><br>

		<span class="title">Why is Kiirti in Beta?</span><br><br><span class="copy">
Though we have been working on the service for past many months, there are a few known issues that were de-prioritized for launch and will be fixed soonafter. We also anticipate few unknown issues to crop up once the service is launched to a wider audience. Keeping these issues in mind, we want to set the right expectations for the users via the Beta tag.
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">Can citizens report about civic issues that is not in their locality?</span><br><br><span class="copy">
Yes. The requirement is to accurately describe the location of the problem for appropriate action.
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">What are the official and mainstream news?</span><br><br><span class="copy">
Each Supported Causes can customize RSS feeds from mainstream news sites based on their area of focus. <br><br>
<i>For example, an anti-corruption organization can setup Google News RSS feeds for articles related to corruption.</i>
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">Can citizens request new Categories for a Supported Cause?</span><br><br><span class="copy">
Yes. Supported Causes are managed by an individual or a group of organizations. You can send a request using the Contact Us form for the specific Supported Cause to add new categories. 
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">Sometimes there are many reports filed under a Supported Cause. How do I find my report or find those that are interesting to me?</span><br><br><span class="copy">
You can use the search box on the Reports page to search for a specific report or those that might be of interest to you (based on keywords in the report).
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">What do you do with my contact information?</span><br><br><span class="copy">
We do not share your contact details, unless required by law, with anyone other than organizations that own or moderate the Supported Causes against which you reported issues. 
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">I reported an issue earlier. How can I modify the details?</span><br><br><span class="copy">
You can add new details against the old report using the comments section. We do not allow modifying or deleting comments and reports unless they're inappropriate.
		</span><br><img src='/media/img/line.gif'><br>
		
		<span class="title">Is there a limitation to the size of the photograph that I want to upload on the site?</span><br><br><span class="copy">
No, there is no limitation to the size of the photograph. Though we highly recommend uploading small to medium sized pictures to allow fast loading for users with limited bandwidth or for those using mobile applications.
